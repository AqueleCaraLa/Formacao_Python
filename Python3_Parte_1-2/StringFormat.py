print('\n')
print('Tentativa {} de {}'.format(3, 10)) #Adiciona os valores nas chaves

print('Tentativa {1} de {0}\n'.format(3, 10)) # Escolha de valores das chaves

print('RS {}'.format(1.59)) #Float no format

print('RS {:.2f}'.format(1.5)) # Escolha de Casas decimais nas chaves
print('RS {:.2f}'.format(1234.5)) 

print('RS {:7.2f}'.format(4.5)) #Fazer o espaçamento
print('RS {:07.2f}\n'.format(4.5)) #Adicionar 0 nos espaçamentos

print('Data: {:02d}/{:02d}'.format(9,4)) # Espaçamento para números inteiros